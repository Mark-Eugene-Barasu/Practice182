# list
my_list = [1, 2, 3, 4, 5]
print(my_list)

print(my_list[0])  # Accessing the first element
print(my_list[2])  # Accessing the third element
print(my_list[-1])  # Accessing the last element
print(my_list[-3])  # Accessing the third element from the end


